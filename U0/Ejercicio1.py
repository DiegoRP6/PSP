#la suma, resta, multiplicación, división y resto de ambos, con el siguiente formato:

x = int(input("Ingrese un número entero: "))
y = int(input("Ingrese otro número entero: "))

print("x + y = ", x + y)
print("x - y =  ", x - y)
print("x * y =  ", x * y)
print("x / y =", x / y)
print("x % y = ", x % y)

